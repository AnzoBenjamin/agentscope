export function getEventMeta(eventType: string) {
  return (
    eventTypeMeta[eventType] ?? {
      label: eventType.replace(/([A-Z])/g, " $1").trim(),
      tone: "muted" as EventTone,
      Icon: HashIcon,
    }
  );
}

export function RiskBadge({
  level,
  className,
}: {
  level: string;
  className?: string;
}) {
  const normalized = level.toLowerCase();
  const tone: EventTone =
    normalized === "high"
      ? "danger"
      : normalized === "medium"
        ? "warning"
        : normalized === "low" || normalized === "none"
          ? "success"
          : "muted";
  const styles = eventToneStyles[tone];
  const Icon =
    tone === "danger"
      ? AlertCircleIcon
      : tone === "warning"
        ? AlertTriangleIcon
        : tone === "success"
          ? ShieldCheckIcon
          : MinusIcon;
  const label = level ? level.charAt(0).toUpperCase() + level.slice(1) : "—";
  return (
    <span
      className={cn(
        "inline-flex items-center gap-1.5 rounded-full border px-2.5 py-1 text-xs font-semibold",
        styles.chip,
        styles.chipText,
        className,
      )}
    >
      <Icon className="size-3.5" />
      {label} risk
    </span>
  );
}
